function koef = mnk()

    x = [0:2000:10000, 15000, 20000, 25000, 30000, 40000]';
    y = [1.225 1.007 0.8194 0.6601 0.5258 0.4135 0.1948 0.08891 0.04008 0.01841 0.003996]';
    
    f1 = @(h) ones(size(h));
    f2 = @(h) ((h - 40000)/40000).^2;
    f3 = @(h) ((h - 40000)/40000).^4;
    
    A = [f1(x) f2(x) f3(x)];
    koef = A\y;

end



